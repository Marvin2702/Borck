export const ui: Record<Lang, Record<UIKey, string>> = { de, en, nl, da };

/** t(lang)('key', {param}) — Interpolation über {name}-Platzhalter. */
export function makeT(lang: Lang) {
  return (key: UIKey, params?: Record<string, string | number>): string => {
    let s: string = ui[lang][key] ?? ui.de[key];
    if (params) {
      for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, String(v));
    }
    return s;
  };
}

export type TFn = ReturnType<typeof makeT>;

/** km-Format: DE/NL/DA mit Komma, EN mit Punkt. */
export function fmtKm(km: number, lang: Lang): string {
  const s = String(km);
  return lang === 'en' ? s : s.replace('.', ',');
}

/** Gerätesprache erkennen (Hermes/Browser liefern Intl) — unbekannt ⇒ de. */
export function detectLang(): Lang {
  try {
    const tag = Intl.DateTimeFormat().resolvedOptions().locale.toLowerCase();
    for (const l of ['de', 'en', 'nl', 'da'] as const) {
      if (tag === l || tag.startsWith(`${l}-`)) return l;
    }
  } catch {
    // Intl nicht verfügbar → Standard
  }
  return defaultLang;
}

